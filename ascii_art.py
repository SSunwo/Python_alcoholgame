#색깔 입히는 함수
def paint(lines=list):
    colors = ["#22577A", "#38A3A5", "#57CC99", "#80ED99", "#99FFED", "#FFFFFF"]
    
    for index, color in enumerate(colors):
        ansi_sequence = f"\033[38;2;{int(color[1:3], 16)};{int(color[3:5], 16)};{int(color[5:7], 16)}m"
        print(ansi_sequence + lines[index] + "\033[0m", end='')
    return

